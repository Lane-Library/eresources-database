package edu.stanford.irt.eresources.redivis;

public class Entity {

    private String name;

    public Entity() {
        // empty constructor
    }

    /**
     * @return the name
     */
    public String getName() {
        return this.name;
    }

    /**
     * @param name
     *            the name to set
     */
    public void setName(final String name) {
        this.name = name;
    }
}
