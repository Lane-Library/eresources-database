package edu.stanford.irt.eresources.redivis;

public class DatasetCollection {

    private String id;

    private String name;

    private String url;

    public DatasetCollection() {
        // empty constructor
    }

    /**
     * @return the id
     */
    public String getId() {
        return this.id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return this.name;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return this.url;
    }
}
